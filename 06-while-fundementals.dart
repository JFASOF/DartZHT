void main(List<String> args) {
  int counter = 10;//counter is a variable
  while (counter > 0) {
    print(counter);
    counter--;
  }
  //do-while
  print('-------------------------------');
  int counter2 = 10;//counter2 is a variable
  do {
    print(counter2);
    counter2--;
  } while (counter2 > 0);
}